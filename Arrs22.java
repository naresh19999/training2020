public class Arrs22 {
	public static void main(String[] args) {
		int arr[]=new int[4];      //One dimensional Array
		arr[0]=22;
		arr[1]=20;
		arr[2]=30;
		arr[3]=222;
		for(int i=0;i<arr.length;i++) {
		System.out.println(arr[i]);
		}
	}
}                                  //Output::  22
                                   //          20
                                   //          30
                                   //          222
