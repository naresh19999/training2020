
public class Three {

	public static void main(String[] args) {            //type casting and increment and decrement
		double d=5;   //implicit conversion
        int k=(int)45.8;//explicit conversion
        System.out.println(k);
        System.out.println(d);
        
        
       int a=k++;   //pre increment
       int b=k--;   //pre drecement
       int c=++k;   //post increment
       int f=--k;   //post decrement
       System.out.println(a);
       System.out.println(b);
       System.out.println(c);
       System.out.println(f);
       
        
	}

}
